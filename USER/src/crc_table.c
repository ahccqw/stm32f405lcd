/* CRC16 计算函数（双表查询法） */
u16 crc16_modbus(uint8_t *data, uint16_t length)
{
    u8 crc_hi = 0xFF;   // 高字节
    u8 crc_lo = 0xFF;   // 低字节
    u8 index;

    while (length--)
    {
				//索引将对应 *data的值 异或上 0xFF 把每个字节的数据的融合进CRC中 又由于modbus中是 LSB发送的格式 低位在前，所以是用 crc的低8位来异或
				//异或不会影响到上一次的数据，如果用 与的话就会覆盖掉前面的数据
        index = crc_lo ^ *data++;      		 // 1. 计算索引
			
				/*CRC 寄存器想象成左右两半：[ 高字节 (Hi) ]	[ 低字节 (Lo) ]
					当处理完一个自己后整个16位寄存器要向右移动8位
					相当于 旧的低八位消失，则需要旧的高八位右移 此时再更新低字节
					而新的高八位重新接收到现在发来8位的数据
					这是由于：modbus 的 crc16是 向右的 LSB，低位在前
				*/
        crc_lo = crc_hi ^ auchCRCHi[index];// 2. 更新低字节
        crc_hi = auchCRCLo[index];			   // 3. 更新高字节	由于右移，原来的高 8 位位置空了出来，直接由查表得出的结果填入
    }

    return ((u16)crc_hi << 8) | crc_lo;
}


